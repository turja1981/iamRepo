Create table T_USER (
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY

CREATE MEMORY TABLE PUBLIC.T_USER(USER_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
NAME VARCHAR(50),
MOBILENO VARCHAR(20),
EMAIL VARCHAR(100),
GENDER VARCHAR(10),
AGE INTEGER,
PROFESSION VARCHAR(50),
EXPERIENCE INTEGER ,
PROFILE VARCHAR(100),
STATUS VARCHAR(20) ,
CREATED_BY VARCHAR(20),
CREATED_ON TIMESTAMP,
UPDATED_BY VARCHAR(20),
UPDATED_ON TIMESTAMP
)


CREATE MEMORY TABLE PUBLIC.T_ADDRESS(
ADRS_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
USER_ID INTEGER FOREIGN KEY  REFERENCES T_USER (USER_ID),
CITY VARCHAR(50),
LOCALITY VARCHAR(50),
ZIP VARCHAR(20),
COUNTRY VARCHAR(50),
CREATED_BY VARCHAR(20),
CREATED_ON TIMESTAMP,
UPDATED_BY VARCHAR(20),
UPDATED_ON TIMESTAMP
)

CREATE MEMORY TABLE PUBLIC.T_JOBQ(
JOB_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
JOB_DESC VARCHAR(100),
REQ_USER_ID  INTEGER,
REQUIRED_TIME TIMESTAMP ,
END_TIME TIMESTAMP ,
STATUS VARCHAR(30),
URGENCY  VARCHAR2(10) ,
CREATED_BY VARCHAR(20),
CREATED_ON TIMESTAMP,
UPDATED_BY VARCHAR(20),
UPDATED_ON TIMESTAMP
)

CREATE MEMORY TABLE PUBLIC.T_JOB_RESPONSE(
RES_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
JOB_ID INTEGER  FOREIGN KEY REFERENCES T_JOBQ (JOB_ID),
RES_USER_ID  INTEGER,
STATUS VARCHAR(30),
CREATED_BY VARCHAR(20),
CREATED_ON TIMESTAMP,
UPDATED_BY VARCHAR(20),
UPDATED_ON TIMESTAMP
)

CREATE MEMORY TABLE PUBLIC.T_MESSAGE(
MSG_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
SRC_USER_ID INTEGER,
DEST_USER_ID INTEGER,
STATUS VARCHAR(30),
MSG_TYPE VARCHAR(30),
START_TIME TIMESTAMP ,
CREATED_BY VARCHAR(20),
CREATED_ON TIMESTAMP,
UPDATED_BY VARCHAR(20),
UPDATED_ON TIMESTAMP
)

CREATE MEMORY TABLE PUBLIC.T_RATING(
RATING_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
RATING_LEVEL VARCHAR(20),
SP_USER_ID INTEGER,
CON_USER_ID INTEGER,
CREATED_BY VARCHAR(20),
CREATED_ON TIMESTAMP,
UPDATED_BY VARCHAR(20),
UPDATED_ON TIMESTAMP
)


